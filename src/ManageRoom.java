    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: new room save button
        String roomNumber = jTextField1.getText();
        String activate;
        String roomStatus = "Not Booked";
        if(jCheckBox1.isSelected()){
            activate="Yes";
        }
        else{
            activate="No";
        }
        try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into room value(?,?,?)");
            ps.setString(1,roomNumber);
            ps.setString(2,activate);
            ps.setString(3,roomStatus);
            
            ps.execute();
            JOptionPane.showMessageDialog(null, "Succsessfully Updated");;
            tableDetails();
            clear();
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here: update room search button
        String roomNumber = jTextField2.getText();
        int i=0;
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from room where number='"+roomNumber+"'");
            while(rs.next()){
                i=1;
                if (rs.getString(3).equals("Booked")){
                    jTextField2.setBackground(Color.red);
                    JOptionPane.showMessageDialog(null,"This room is already Booked");
                    tableDetails();
                    clear();
                }
                else{
                    jTextField2.setBackground(Color.green);
                    if(rs.getString(2).equals("Yes")){
                        jCheckBox2.setSelected(true);
                      
                    }
                    else{
                        jCheckBox2.setSelected(false);
                    }
                }
            }
            if(i==0){
                JOptionPane.showMessageDialog(null, "Room number doen not Exist");
                tableDetails();
                clear();
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: update section delete button
         String roomNumber = jTextField2.getText();
         int i=0;
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from room where number='"+roomNumber+"'");
            while(rs.next()){
                i=1;
                if (rs.getString(3).equals("Not Booked")){
                    st.executeUpdate("delete from room where number = '"+roomNumber+"'");
                    JOptionPane.showMessageDialog(null,"Successfully Deleted");
                    tableDetails();
                    clear();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Room is already booked you can not delete it");
                }
            }
             if(i==0){
                JOptionPane.showMessageDialog(null, "Room number doen not Exist");
                tableDetails();
                clear();
            }
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

