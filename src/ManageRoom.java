    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: Update button
        String roomNumber = jTextField2.getText();
        String activate;
        if(jCheckBox2.isSelected()){
            activate="Yes";
        }
        else{
            activate="No";
        }
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from room where number='"+roomNumber+"' and roomStatus='Booked'");
            if (rs.next()){
                JOptionPane.showMessageDialog(null,"Room is alredy booked you can't update it.");
            }
            else{
                st.executeUpdate("update room set activate='"+activate+"' where number='"+roomNumber+"'");
                JOptionPane.showMessageDialog(null,"Successfully Updated");
                tableDetails();
                clear();
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

